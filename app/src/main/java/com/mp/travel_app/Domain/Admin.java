package com.mp.travel_app.Domain;

public class Admin {
    private int id;
    private String fullname, phoneNumber, email, avatar, username, password;

    public Admin() {
    }

    public Admin(int id, String fullname, String phoneNumber, String email, String avatar, String username, String password) {
        this.id = id;
        this.fullname = fullname;
        this.phoneNumber = phoneNumber;
        this.email = email;
        this.avatar = avatar;
        this.username = username;
        this.password = password;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getFullname() {
        return fullname;
    }

    public void setFullname(String fullname) {
        this.fullname = fullname;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getAvatar() {
        return avatar;
    }

    public void setAvatar(String avatar) {
        this.avatar = avatar;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    @Override
    public String toString() {
        return id + " - " + fullname;
    }
}
